 // Función para encontrar el índice de una fecha en el arreglo de fechas
function findIndexByDate(dateArray, targetDate) {
    for (let i = 0; i < dateArray.length; i++) {
        if (dateArray[i] === targetDate) {
            return i;
        }
    }
    return -1;
}